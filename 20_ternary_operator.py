# Ternary operator or conditional expression evaluates to a statement if it is true else it evaluates other statement

fsalary = int(input('Enter your father\'s salary:'))
msalary = int(input('Enter your mother\'s salary:'))

print('Father\'s salary is greater than Mother\'s salary' if fsalary > msalary else 'Mother\'s salary is greater than Father\'s salary')

# salary = fsalary > msalary : 'Father\'s salary is greater than Mother\'s salary';'Mother\'s salary is greater than Father\'s salary'